export async function getServerSideProps({ params }) {
  console.log("params:", params);
  const res = await fetch(
    `https://jsonplaceholder.typicode.com/todos/${params.id}`
  );

  const post = await res.json();
  console.log("post:", post);
  if (res.status !== 200) {
    return {
      notFound: true,
    };
  }
  return {
    props: { post },
  };
}

